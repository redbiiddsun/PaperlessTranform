def extract_labels(text):
    # Define a regex pattern to capture field labels
    pattern = r'([\u0E00-\u0E7F]+)(?=[\.\:])'  # Matches Thai characters before '.' or ':'
    labels = re.findall(pattern, text)
    return labels



# Extract and print the labels
labels = extract_labels(thai_text)

for i in range(1, len(labels)):
    if len(labels[i]) == 1 and len(labels[i-1]) == 1:
        labels[i-1] = labels[i-1] + '.' + labels[i] + '.'
        labels[i] = ''
        
labels = [label for label in labels if label != '']

print(labels)